						<h4>Ensayos No Destructivos por Partículas Magnéticas</h4>
						<p>Los servicios de ensayos no destructivos (END) por partículas magnéticas le ayudan a detectar discontinuidades superficiales y sub-superficiales en materiales ferromagnéticos.</p>
						<p>Dentro de los ensayos superficiales, el ensayo mediante partículas magnéticas de materiales ferromagnéticos presenta una ventaja sustancial respecto al resto ya que es capaz de detectar defectos subsuperficiales, es decir, aquellos que no afloran a la superficie pero están cercanos a ella. Esta capacidad permite la inspección de materiales con recubrimientos (finas capas de pintura, imprimaciones, etc).</p>
						<p>Los ensayos por partículas magnéticas tienen una extensa aplicación en los procesos de fabricación y en la inspección en servicio, entre las que se encuentran:</p>
						<ul>
							<li>Inspección de soldaduras.</li>
							<li>Inspección de preparaciones de bordes para soldadura o de excavaciones en reparación de las mismas.</li>
							<li>Inspección de componentes metálicos: forjas, fundiciones, mecanizados, etc.</li>
