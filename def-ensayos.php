							<h4>Ensayos No Destructivos por Líquidos Penetrantes</h4>
							<p>La inspección con Líquidos Penetrantes es una de las técnicas de Ensayos no Destructivos para detectar discontinuidades, defectos y grietas.</p>
							<p>Algunas discontinuidades o defectos producidos durante las operaciones de laminación, forja o fundición, etc., tienen siempre apertura  a la superficie, y se pueden detectar desde el momento en que se han producido; sin embargo discontinuidades o defectos que están por debajo de la superficie del material en una etapa de la producción, pueden aparecer abiertas a la superficie en la etapa siguiente, siendo ya detectables con Líquidos penetrantes.</p>
